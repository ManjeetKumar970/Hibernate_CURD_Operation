package org.HibernateCURDOperation;

public class UserRegistor {
	private int id;
	private String name;
	private String Addresh;
	private String State;
	private String city;
	private long Mobile;
	private String straem;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddresh() {
		return Addresh;
	}
	public void setAddresh(String addresh) {
		Addresh = addresh;
	}
	public String getState() {
		return State;
	}
	public void setState(String state) {
		State = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public long getMobile() {
		return Mobile;
	}
	public void setMobile(long mobile) {
		Mobile = mobile;
	}
	public String getStraem() {
		return straem;
	}
	public void setStraem(String straem) {
		this.straem = straem;
	}
	

}
